const USED_NAMES = new Set<string>();
export function generatePirateName(): string {
  let randomString;
  while (randomString == null || USED_NAMES.has(randomString)) {
    randomString = `${getRandomElement(ADJECTIVES)} ${getRandomElement(FIRST_NAMES)}`;
  }
  USED_NAMES.add(randomString);
  return randomString;
}

